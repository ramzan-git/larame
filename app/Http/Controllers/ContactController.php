    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'company_name'=>'required',
            'contact_person_name'=>'required',
            'person_mobile'=>'required'
        ]);

        $contact = new Contact([
            'company_name' => $request->get('company_name'),
            'contact_person_name' => $request->get('contact_person_name'),
            'person_mobile' => $request->get('person_mobile'),
            'person_email' => $request->get('person_email'),
            'office_addr' => $request->get('office_addr'),
            'company_balance' => $request->get('company_balance')
        ]);
        $contact->save();
        return redirect('/contacts')->with('success', 'Contact saved!');
    }

